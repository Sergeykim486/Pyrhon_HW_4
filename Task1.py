# 1'. Вычислить число Пи c заданной точностью d

# *Пример:* 

# - при d = 0.001, π = 3.141
# - при d = 0.0001, π = 3.1415  
import math
d = str(input('Введите число d, \nв формате 0,XXX (любое количество чисел после запятой) ->  '))
d.split('.')[1]
n = ''
f = 0
for i in d:
    if f == 1:
        n = n + i
    if i == '.':
        f = 1

print(round(math.pi, len(n)))